public class Group {
    String name;
    int cnt;
    public String getName() {return name;}
    public int getCnt() {return cnt;}
    public void setName(String name) {this.name = name;}
    public void setCnt(int cnt) {this.cnt = cnt;}
    public Group (String name, int cnt) {
        this.name = name;
        this.cnt = cnt;
    }
}
